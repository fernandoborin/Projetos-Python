import math
num = float(input('Digite um número real: '))
print('O valor digitado foi {} e sua versão inteira é {} (floor)'.format(num, math.floor(num)))
print('O valor digitado foi {} e sua versão inteira é {} (trunc)'.format(num, math.trunc(num)))
print('O valor digitado foi {} e sua versão inteira é {} (int)'.format(num, int(num)))
